/**
 * LV2コンポーネントの「構築と配線の分離」を支える配線保持セル。
 *
 * Why: イベントハンドラ群をコンストラクタで受け取る方式は、Orchestratorの
 * コンストラクタを巨大な配線儀式にし、相互参照する部品間（例: タイムラインと
 * 再生コントローラ）で「まだ初期化されていないフィールドをクロージャで包んで誤魔化す」
 * 初期化順の歪みを生む。部品はコンストラクタで見た目だけを構築し、コンポジション
 * ルート（Orchestrator）が全部品の構築後に配線オブジェクトを1回だけ渡す。
 *
 * 配線忘れの検出は2層で保証する:
 * - 配線オブジェクト内のハンドラ漏れ → 必須プロパティのインターフェースでコンパイルエラー
 * - 配線する() 自体の呼び忘れ・二重呼び → 本クラスが実行時に例外で明示する
 *   （オプショナルコールバックや Null Object での無言の無反応は silent failure であり禁止）
 */
/**
 * 「配線する() を持つ部品」であることを表明する規約マーカー。
 * Why: 構造的型付けのTypeScriptでは implements なしでも適合するが、明示することで
 * 新配線規約（第11条）準拠の部品であることが宣言され、コンポジションルート側の
 * 一括処理（配線済み検査等）の型的土台になる。
 * 戻り値は this — ルート構築ツリーの中で「配置しながら配線する」チェーンを可能にする
 */
export interface I配線可能<T> {
    配線する(配線: T): this;
}


export class 配線ポート<T> {
    private _配線: T | null = null;

    constructor(private readonly _所有者名: string) {}

    配線する(配線: T): void {
        if (this._配線 !== null) {
            throw new Error(`${this._所有者名} は配線済みです。配線する() はコンポジションルートから1回だけ呼べます`);
        }
        this._配線 = 配線;
    }

    /**
     * 配線先を取得する。イベントハンドラ内から `this._配線.先.onXxx(...)` の形で使う。
     * Why: 未配線のままユーザー操作が届いた場合、既定値適用や無視で誤魔化さず
     * 例外で「配線が組まれていない」ことを即座に表面化させる
     */
    get 先(): T {
        if (this._配線 === null) {
            throw new Error(`${this._所有者名} が未配線のまま操作されました。コンポジションルートで 配線する() を呼んでください`);
        }
        return this._配線;
    }

    get 配線済みか(): boolean {
        return this._配線 !== null;
    }
}
